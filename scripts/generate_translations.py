def translate_text(text, lang_dict):
    """Translate English text using phrase-level dictionary.
    Preserves ${vars}, <html> tags, emojis, and technical terms."""
    result = text
    
    # Sort by phrase length (longest first) to avoid partial matches
    sorted_phrases = sorted(lang_dict.items(), key=lambda x: len(x[0]), reverse=True)
    
    for en_phrase, translated in sorted_phrases:
        # Only replace if it's a whole word/phrase match (not inside another word)
        pattern = r'(?<![a-zA-Z])' + re.escape(en_phrase) + r'(?![a-zA-Z])'
        result = re.sub(pattern, translated, result)
    
    return result

